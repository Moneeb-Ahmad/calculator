let x = 5;
let y = 2;
console.log(add(x,y));
console.log(subtract(x,y));
console.log(multiply(x,y));
console.log(divide(x,y));

function add(n1, n2) {
  return n1 + n2;
}

function subtract(n1, n2) {
  return n1 - n2;
}

function multiply(n1,n2) {
  return n1 * n2;
}

function divide(n1, n2) {
  return n1 / n2;
}
